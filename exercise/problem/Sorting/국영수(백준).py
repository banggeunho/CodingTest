# 백준 10825
# 국영수
# 파이썬 라이브러리 올바른 사용법에 대한 문제 같음


n = int(input())
score = []

for i in range(n):
  a, b, c, d = input().split()
  score.append([a, int(b), int(c), int(d)])

f = sorted(score, key = lambda x: (-x[1], x[2], -x[3], x[0]))

for i in f:
  print(i[0])
  